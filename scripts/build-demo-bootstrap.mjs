import { readdir, readFile, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const repoRoot = path.resolve(__dirname, '..')
const migrationsDir = path.join(repoRoot, 'migrations')
const seedPath = path.join(__dirname, 'demo-seed.sql')
const outputPath = path.join(__dirname, 'demo-bootstrap.sql')

const migrationFiles = (await readdir(migrationsDir))
  .filter((file) => file.endsWith('.sql'))
  .sort((a, b) => a.localeCompare(b))

const lineSkips = new Map([
  [
    '0014_santri_keluar.sql',
    [
      "ALTER TABLE santri ADD COLUMN tanggal_keluar TEXT;",
    ],
  ],
])

const chunks = [
  '-- Auto-generated by scripts/build-demo-bootstrap.mjs',
  '-- Rebuild this file with: npm run demo:build-sql',
  'PRAGMA foreign_keys = OFF;',
]

for (const file of migrationFiles) {
  const rawContent = await readFile(path.join(migrationsDir, file), 'utf8')
  const skipLines = lineSkips.get(file) ?? []
  const content = rawContent
    .split(/\r?\n/)
    .filter((line) => !skipLines.includes(line.trim()))
    .join('\n')
  chunks.push(`\n-- >>> ${file}\n`)
  chunks.push(content.trim())
  chunks.push('\n')
}

chunks.push('\n-- >>> demo-seed.sql\n')
chunks.push((await readFile(seedPath, 'utf8')).trim())
chunks.push('\nPRAGMA foreign_keys = ON;\n')

await writeFile(outputPath, chunks.join('\n'), 'utf8')
console.log(`Demo bootstrap SQL written to ${outputPath}`)
